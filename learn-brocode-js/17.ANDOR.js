// && AND = (BOTH Condition must be true)
// || OR  = (1 Condition must be true)

// 1. AND
let temp = 15;
if (temp > 0 && temp < 30) {
    console.log('The weather is Good');
} else {
    console.log('the weather is bad');
}
// 2. OR
let temp2 = 10;
if (temp2 == 10 || temp2 > 30 || temp2 == 20) {
    console.log('The weather is Good');
} else {
    console.log('the weather is bad');
} 